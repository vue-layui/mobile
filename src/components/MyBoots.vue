    import $ from 'jquery'
    export default {
      name: "MyBoots",
      data(){
        return{
          scrollState: true, //是否可以滑动
          indexScrollTop:0,
          listdata:[]
        }
      },
      components: {
        // myScroll
      },
      mounted(){
        // 遍历样式
        $(".px").each(function(){
          $(".px span").each(function(){
            if($(".px span:empty")){
              $($(".px span:empty")).css({
                display:"none"
              })
            }
          });
        });
        $(".ps").each(function(){
          $(".ps span:empty").css({
            display:"none"
          })
        });

      },




      // 下拉刷新上拉加载
      methods:{
        onRefresh(mun) { // 刷新
          this.listParams.p = 1;
          this.$axios
            .get(apiUrl.noticeList, {
              params: this.listParams,
              isLoading: false
            })
            .then(res => {
              if (res.code == 10000) {
                this.listParams.p++;
                this.listdata = res.data;
                this.$refs.myScroll.setState(3);
              } else {
                this.$refs.myScroll.setState(3);
              }
            });
        },
        onPull(mun) { //加载
          this.$axios
            .get(apiUrl.noticeList, {
              params: this.listParams,
              isLoading: false
            })
            .then(res => {
              if (res.code == 10000 && res.data.length > 0) {
                this.listParams.p++;
                res.data.map((v, k) => {
                  this.listdata.push(v);
                });
                this.$refs.myScroll.setState(5);
              } else {
                this.$refs.myScroll.setState(7);
              }
            });
        },
        getTop(y) {//滚动条位置
          this.indexScrollTop = y;
        },
      },

    }
</script>

